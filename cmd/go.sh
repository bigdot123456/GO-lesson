#/usr/loca/go

..build    #用于编译给定的代码包或者Go语言源码文件及其依赖包
..clean	   #用于清除执行其他go命令遗留的目录和文件
..env	   #用于打印go语言环境信息
..fix	   #用于执行go tool fix命令已修正给定的代码包的源码文件中包含的过时语		   法和代码调用
..fmt      #用于执行gofmt命令已格式化给定代码包中的源码文件
..get	   #用于下载和安装给定代码包及其依赖包
..install  #用于编译和安装给定的代码包及其依赖项
..list     #用于显示给定代码包的信息
..run	   #用于编译并运行给定的命令源码文件 
..test     #用于测试给定的代码包
..tool     #用于运行Go语言的特殊工具
..version  #用于显示当前安装的Go语言的版本信息
